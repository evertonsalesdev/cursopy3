# Tipo de dados bool(Boolean)
# Ao questinar algo em um programa, só existem duas respostas possíveis:
# Sim (True) ou não (False)
# Existem vários operadores para "questionar"
# Dentre eles o ==, que é um operador lógico,
# Questiona se um valor é igual ao outro.

print(10==10) # Sim => True (Verdadeiro)

print(10==11) # Não => False (Falso)

print(type(10==10))